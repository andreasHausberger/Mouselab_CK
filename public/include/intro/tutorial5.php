        <p class="textSpan">
            This study looks at the tax compliance behavior.
        </p>

        <p class="textSpan">
            In the study you will face multiple decision rounds. For each round you receive a <b>guaranteed income
                of 1000 ECU </b>(experimental currency units). In addition, you can earn an <b>extra income</b> of
            up to <b>1000 ECU </b>based on your performance in an effort task. This results in a possible total
            income of 2000 ECU per round. You will then be required to pay taxes on this earned income.
        </p>
        <p class="textSpan">
            The <b>tax due</b>, the <b>fine level</b>, and your <b>chance of getting audited</b> (chance of getting
            checked) will <b>vary</b> each round, so carefully read all the information. There are about 20 rounds.
        <p class="textSpan">
            <i>Definitions: </i>
        </p>
        <p class="textSpan">
            Tax rate is the percentage of your income you are required to pay as tax. It determines the
            <b>tax due</b>, which is the actual amount you are asked to pay as tax.
        </p>
        <p class="textSpan">
            An audit can take place to check whether you have paid all tax due. The <b>audit probability</b> (chance
            of getting audited) is provided as a percentage. In case you completely paid the tax due, an audit is
            without any consequences. In case you have evaded tax and you are audited, you have to pay back the
            missing amount plus a fine (see below). Information on whether you have been audited and whether this
            audit results in a fine will be communicated after each round.
        </p>
        <p class="textSpan">
            <b>Fine</b> is what you would have to pay in case you were audited and evaded tax. It consists of paying
            back the evaded tax plus an additional fine which is proportional to the evaded amount.
        </p>



